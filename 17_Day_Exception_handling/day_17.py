names = ['Finland', 'Sweden', 'Norway', 'Denmark', 'Iceland', 'Estonia', 'Russia']

# Unpacking
*nordic_countries, es, ru = names

print("Nordic countries:", nordic_countries)
print("Estonia:", es)
print("Russia:", ru)

#output:-
#   Nordic countries: ['Finland', 'Sweden', 'Norway', 'Denmark', 'Iceland']
#   Estonia: Estonia
#   Russia: Russia